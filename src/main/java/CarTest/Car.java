package CarTest;

public class Car {

    public int wheel;
    public int speed;



    public int getWheel() {

        return wheel;
    }

    public void setWheel(int wheel) {

        this.wheel = this.wheel;
    }
    public int getSpeed() {

        return speed;
    }
    public void setSpeed(int speed) {

        this.speed = this.speed;
    }

    public void Car(int speed, int wheel) {
        this.speed = speed;
        this.wheel = wheel;
    }

}